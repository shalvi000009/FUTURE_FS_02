// Admin: update product (supports optional new images)
const updateProduct = async (req, res) => {
  try {
    const {
      id,
      name,
      description,
      price,
      category,
      subCategory,
      sizes,
      bestseller,
    } = req.body;

    const existing = await productModel.findById(id);
    if (!existing) {
      return res.status(404).json({ success: false, message: "Product not found" });
    }

    const image1 = req.files?.image1 && req.files.image1[0];
    const image2 = req.files?.image2 && req.files.image2[0];
    const image3 = req.files?.image3 && req.files.image3[0];
    const image4 = req.files?.image4 && req.files.image4[0];

    const images = [image1, image2, image3, image4].filter((item) => item !== undefined);
    const imagesUrl = images.length ? images.map((item) => item.filename) : existing.image;

    const update = {
      name: name ?? existing.name,
      description: description ?? existing.description,
      category: category ?? existing.category,
      subCategory: subCategory ?? existing.subCategory,
      price: price !== undefined && price !== "" ? Number(price) : existing.price,
      bestseller:
        bestseller !== undefined ? (bestseller === "true" || bestseller === true) : existing.bestseller,
      sizes: sizes ? (Array.isArray(sizes) ? sizes : JSON.parse(sizes)) : existing.sizes,
      image: imagesUrl,
    };

    await productModel.findByIdAndUpdate(id, update);
    res.json({ success: true, message: "Product Updated" });
  } catch (error) {
    console.log(error);
    res.json({ success: false, message: error.message });
  }
};

export { listProducts, addProduct, removeProduct, singleproduct, updateProduct };